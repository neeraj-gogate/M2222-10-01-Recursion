func fibonacci(number: Int) -> Int {
    print("Calculating fibonacci(\(number))...")
    let fib = number <= 2 ? 1 : fibonacci(number:number-1) + fibonacci(number:number-2)
    let term = number <= 2 ? "Terminal" : "Non-terminal"
    print("\(term) case, returning \(fib).")
    return fib
}
